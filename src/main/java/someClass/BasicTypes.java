package someClass;

public class BasicTypes {
    public static void main(String[] args) {
        Integer a = 0;
        Short b = 0;
        Character c = 'A';
        Double d = 0.0;
        Float f  = 0.0f;
        Byte by = 0;
        Long l = 0l;
        Boolean blo = false;
        System.out.println("Integer     :"+a.MAX_VALUE);
        System.out.println("Short       :"+b.MAX_VALUE);
        System.out.println("Character       :"+c.MAX_VALUE);
        System.out.println("Double      :"+d.MAX_VALUE);
        System.out.println("Float       :"+f.MAX_VALUE);
        System.out.println("Byte        :"+by.MAX_VALUE);
        System.out.println("Long        :"+l.MAX_VALUE);
        System.out.println("Boolean     :"+blo);
    }
}
